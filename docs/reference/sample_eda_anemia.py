"""## Hot- and cold-spot analyses 🔥❄️"""

# Calculate local Moran's I
w = Queen.from_dataframe(gdf)
w.transform = 'r'
moran_loc = Moran_Local(gdf['prevalence'], w)

# Add local Moran's I values and significance to the geodataframe
gdf['moran_local'] = moran_loc.Is
gdf['p_sim'] = moran_loc.p_sim
gdf['sig'] = moran_loc.p_sim < 0.05

# Define cluster types
gdf['cluster'] = 'Non-significant'
gdf.loc[(moran_loc.q == 1) & (gdf['sig']), 'cluster'] = 'High-High'  # High-High
gdf.loc[(moran_loc.q == 3) & (gdf['sig']), 'cluster'] = 'Low-Lo'  # Low-Low
gdf.loc[(moran_loc.q == 2) & (gdf['sig']), 'cluster'] = 'Low-High'  # Low-High
gdf.loc[(moran_loc.q == 4) & (gdf['sig']), 'cluster'] = 'High-Low'  # High-Low

# Plotting the clusters
fig, ax = plt.subplots(1, 1, figsize=(15, 10))
gdf.boundary.plot(ax=ax)
gdf.plot(column='cluster', ax=ax, legend=True,
         legend_kwds={'title': "Cluster Type"},
         cmap='Set1',
         categorical=True)

# Add state labels
for x, y, label in zip(gdf.geometry.centroid.x, gdf.geometry.centroid.y, gdf['NAME_1']):
    ax.text(x, y, label, fontsize=8, ha='center', color='black')

# Customize plot
ax.set_title("Local Moran's I Clusters for Anemia Prevalence by State in Nigeria", fontdict={'fontsize': '15', 'fontweight' : '3'})
ax.axis('off')

# Save the plot

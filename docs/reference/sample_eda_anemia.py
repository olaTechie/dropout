"""## Funnel plot 🔄"""

# Create the funnel plot
fig = go.Figure()

# Add scatter plot for the data points
fig.add_trace(go.Scatter(
    x=n,
    y=percentage_unvaccinated,
    mode='markers+text',
    name='States',
    text=Name,
    textposition='top center',
    marker=dict(size=8, color='black'),
    textfont=dict(size=8, color='black')
))

# Calculate and add funnel lines
p = np.mean(r) / np.mean(n)
se = np.sqrt(p * (1 - p) / n)

fig.add_trace(go.Scatter(x=n, y=p * 100 + 2 * se * 100, mode='lines', name='95% CI', line=dict(color='blue', width=3, dash='dash')))
fig.add_trace(go.Scatter(x=n, y=p * 100 - 2 * se * 100, mode='lines', name='95% CI', line=dict(color='blue', width=3, dash='dash'), showlegend=False))
fig.add_trace(go.Scatter(x=n, y=p * 100 + 3 * se * 100, mode='lines', name='99.7% CI', line=dict(color='red', width=3, dash='dot')))
fig.add_trace(go.Scatter(x=n, y=p * 100 - 3 * se * 100, mode='lines', name='99.7% CI', line=dict(color='red', width=3, dash='dot'), showlegend=False))
